public class PaireChaineEntier {
    final private String chaine;
    final private int entier;

    public PaireChaineEntier(String chaine, int entier) {
        this.chaine = chaine;
        this.entier = entier;
    }

    public int getEntier() {
        return entier;
    }

    public String getChaine() {
        return chaine;
    }
}


